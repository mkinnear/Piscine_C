#include <unistd.h>

int		ft_iterative_factorial(int nb)
{
	int number;
	int result;

	number = 1;
	result = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (number <= nb)
	{
		result *= number;
		number++;
	}
	return (result);
}
